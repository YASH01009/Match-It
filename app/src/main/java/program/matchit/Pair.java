package program.matchit;

public class Pair {
    private String str;
    private int id;

    public Pair(String str, int id) {
        this.str = str;
        this.id = id;
    }

    public Pair() {
    }

    public String getStr() {
        return str;
    }

    public int getId() {
        return id;
    }
}
